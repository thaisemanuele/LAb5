    private void inserirDesp_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirDesp_ButtonActionPerformed
        if(inserirDesp_Evento.getSelectedIndex()==0||
                inserirDesp_Edicao.getSelectedIndex()==0
                || inserirDesp_Pat.getSelectedIndex()==0) return;
        try {
            Integer codEv = EventoBD.getCodeByName(inserirDesp_Evento.getSelectedItem().toString());
            Integer numEd = Integer.parseInt(inserirDesp_Edicao.getSelectedItem().toString());
            String cnpjPat = PatrocinadorBD.getCnpjByName(inserirDesp_Pat.getSelectedItem().toString());
            java.sql.Date dataDesp = new java.sql.Date(inserirDesp_Data.getDate().getTime());
            String valorDesp = inserirDesp_Valor.getText();
            String descricaoDesp = inserirDesp_Desc.getText();
            if(valorDesp.startsWith("£")||(valorDesp.startsWith("$"))||(valorDesp.startsWith("€"))){
                System.out.println(valorDesp);
                valorDesp = valorDesp.substring(1);
                if(valorDesp.contains(",")){
                    valorDesp = valorDesp.replace(",", "");
                }
            }
            else if(valorDesp.contains("R$ ")){
                valorDesp = valorDesp.substring(3);
                if(valorDesp.contains(",")){
                    valorDesp = valorDesp.replace(",", ".");
                }
            }
            else if(valorDesp.contains(",")){
                valorDesp = valorDesp.replace(",", "");
            }
            
            System.out.println(valorDesp);
            Double valor = Double.parseDouble(valorDesp);
            
            DespesaBD.inserir(codEv, numEd, cnpjPat, dataDesp.toString(), valor.toString(), descricaoDesp);
            JOptionPane.showMessageDialog(null, "Despesa inserida " , "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            inserirDesp_Evento.setSelectedIndex(0);
            inserirDesp_Edicao.setSelectedIndex(0);
            inserirDesp_Pat.setSelectedIndex(0);
            clearFields();
        } catch (SQLException ex) {
            Logger.getLogger(TelaInscrito.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Valor da despesa incorreto " , "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaInscrito.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_inserirDesp_ButtonActionPerformed

