    private void editarDesp_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarDesp_ButtonActionPerformed
        if(editarDesp_Evento.getSelectedIndex()==0||
                editarDesp_Edicao.getSelectedIndex()==0
                || editarDesp_Pat.getSelectedIndex()==0
                || editarDesp_Cod.getSelectedIndex()==0) return;
        try {
            
            Integer codEv = EventoBD.getCodeByName(editarDesp_Evento.getSelectedItem().toString());
            Integer numEd = Integer.parseInt(editarDesp_Edicao.getSelectedItem().toString());
            Integer codDesp = Integer.parseInt(editarDesp_Cod.getSelectedItem().toString());
            java.sql.Date date = new java.sql.Date(editarDesp_Data.getDate().getTime());
            String valorDesp = editarDesp_Valor.getText();
            String descricaoDesp = editarDesp_Desc.getText();
            if(valorDesp.startsWith("£")||(valorDesp.startsWith("$"))||(valorDesp.startsWith("€"))){
                System.out.println(valorDesp);
                valorDesp = valorDesp.substring(1);
                if(valorDesp.contains(",")){
                    valorDesp = valorDesp.replace(",", "");
                }
            }
            else if(valorDesp.contains("R$ ")){
                valorDesp = valorDesp.substring(3);
                if(valorDesp.contains(",")){
                    valorDesp = valorDesp.replace(",", ".");
                }
            }
            else if(valorDesp.contains(",")){
                valorDesp = valorDesp.replace(",", "");
            }
            
            System.out.println(valorDesp);
            Double valor = Double.parseDouble(valorDesp);
            DespesaBD.atualizar(codDesp, codEv, numEd, date.toString(), valor.toString(), descricaoDesp);
            JOptionPane.showMessageDialog(null, "Valores Atualizados " , "Atualizar", JOptionPane.INFORMATION_MESSAGE);
            editarDesp_Evento.setSelectedIndex(0);
            editarDesp_Edicao.setSelectedIndex(0);
            editarDesp_Pat.setSelectedIndex(0);
            editarDesp_Cod.setSelectedIndex(0);
            clearFields();
        } catch (SQLException ex) {
            Logger.getLogger(TelaDespesas.class.getName()).log(Level.SEVERE, null, ex);
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Valor da despesa incorreto " , "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaInscrito.class.getName()).log(Level.SEVERE, null, e);
        }
            
    }//GEN-LAST:event_editarDesp_ButtonActionPerformed

